	function buildPreviewManifest() {
		const scenes = [];
		(state.summary.results || []).forEach((scene,sceneIndex) => {
			const outputs = [];
			for (const image of scene.array || []) {
				const variants = [];
				if (image.render)
					variants.push({ identifier: "render", label: "Render", image: artifactUrl(image.render) });
				if (image.reference)
					variants.push({ identifier: "reference", label: "Reference", image: artifactUrl(image.reference) });
				if (image.difference)
					variants.push({ identifier: "difference", label: "Difference", image: artifactUrl(image.difference) });
				if (variants.length)
					outputs.push({
						identifier: image.identifier,
						title: image.title || image.identifier,
						status: image.status,
						variants
					});
			}
			if (outputs.length)
				scenes.push({
					index: sceneIndex,
					title: scene.display_name || scene.scene_name || "scene",
					sensor: scene.sensor || 0,
					outputs
				});
		});
		return { scenes };
	}

