	function sci(value) {
		const number = Number(value || 0);
		return number.toExponential(3);
	}

	function formatBytes(value) {
		const number = Number(value || 0);
		if (!Number.isFinite(number) || number <= 0)
			return "unknown";
		const units = ["B","KiB","MiB","GiB","TiB"];
		let scaled = number;
		let unit = 0;
		while (scaled >= 1024 && unit + 1 < units.length) {
			scaled /= 1024;
			++unit;
		}
		return scaled.toFixed(unit ? 1 : 0).replace(/\.0$/,"") + " " + units[unit];
	}

	function installHintTooltip() {
		const tooltip = el("div",{ className: "hint-tooltip" });
		tooltip.hidden = true;
		document.body.appendChild(tooltip);
		let activeTarget = null;

		function rootRem() {
			return Number.parseFloat(getComputedStyle(document.documentElement).fontSize) || 16;
		}

		function rem(value) {
			return value*rootRem();
		}

		function lengthFromPixels(value) {
			return (value/rootRem()).toFixed(4).replace(/\.?0+$/,"") + "rem";
		}

		function position(target) {
			if (!target || tooltip.hidden)
				return;
			const rect = target.getBoundingClientRect();
			const margin = rem(0.625);
			const width = tooltip.offsetWidth || rem(20);
			const height = tooltip.offsetHeight || rem(3);
			let left = rect.left;
			let top = rect.bottom + margin;
			if (left + width > window.innerWidth - margin)
				left = window.innerWidth - width - margin;
			if (top + height > window.innerHeight - margin)
				top = rect.top - height - margin;
			tooltip.style.left = lengthFromPixels(Math.max(margin,left));
			tooltip.style.top = lengthFromPixels(Math.max(margin,top));
		}

		function show(target) {
			const hint = target && target.dataset ? target.dataset.hint : "";
			if (!hint)
				return;
			activeTarget = target;
			tooltip.textContent = hint;
			tooltip.hidden = false;
			position(target);
		}

		function hide(target) {
			if (target && activeTarget && target !== activeTarget)
				return;
			activeTarget = null;
			tooltip.hidden = true;
		}

		document.addEventListener("pointerover",(event) => {
			const target = event.target.closest("[data-hint]");
			if (target)
				show(target);
		});
		document.addEventListener("pointerout",(event) => {
			const target = event.target.closest("[data-hint]");
			if (target && !target.contains(event.relatedTarget))
				hide(target);
		});
		document.addEventListener("focusin",(event) => {
			const target = event.target.closest("[data-hint]");
			if (target)
				show(target);
		});
		document.addEventListener("focusout",(event) => {
			const target = event.target.closest("[data-hint]");
			if (target)
				hide(target);
		});
		window.addEventListener("scroll",() => position(activeTarget),true);
		window.addEventListener("resize",() => position(activeTarget));
	}

