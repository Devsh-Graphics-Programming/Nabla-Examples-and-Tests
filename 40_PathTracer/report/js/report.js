	function normalizeStatus(status) {
		return String(status || "not-checked").toLowerCase();
	}

	function statusClass(status) {
		const value = normalizeStatus(status);
		if (value === "passed")
			return "passed";
		if (value === "failed")
			return "failed";
		if (value === "error")
			return "error";
		if (value === "missing-reference" || value === "missing-render")
			return "missing";
		if (value === "not-checked")
			return "not-checked";
		return "unchecked";
	}

	function isProblemStatus(status) {
		return normalizeStatus(status) !== "passed";
	}

	function effectiveSceneStatus(scene) {
		const images = scene && scene.array ? scene.array : [];
		let checked = false;
		let unchecked = false;
		for (const image of images) {
			const status = normalizeStatus(image.status);
			if (status === "passed" || status === "failed")
				checked = true;
			if (status === "not-checked")
				unchecked = true;
			if (status === "failed" || status === "error" || status === "missing-render" || status === "missing-reference")
				return "failed";
		}

		const status = normalizeStatus(scene && scene.status);
		if (status === "failed" || status === "error" || status === "missing-render" || status === "missing-reference")
			return "failed";
		if (!checked && unchecked)
			return "not-checked";
		if (status === "not-checked")
			return "not-checked";
		return status === "passed" ? "passed":"not-checked";
	}

	function statusHint(status) {
		const value = normalizeStatus(status);
		if (value === "missing-render")
			return HINTS.missingRender;
		if (value === "missing-reference")
			return HINTS.missingReference;
		if (value === "not-checked")
			return "This output is present in the report but is intentionally not part of pass/fail validation.";
		if (value === "failed")
			return "This output failed validation or the scene has at least one failing output.";
		if (value === "passed")
			return "This output passed validation.";
		return "Report status recorded for this output.";
	}

	function sceneHasProblem(scene) {
		if (isProblemStatus(effectiveSceneStatus(scene)))
			return true;
		return (scene.array || []).some((image) => isProblemStatus(image.status));
	}

