	function renderOverview() {
		const allResults = state.summary.results || [];
		const sceneEntries = allResults
			.map((scene,sceneIndex) => ({ scene, sceneIndex }))
			.filter((entry) => state.overviewFilter !== "failed" || sceneHasProblem(entry.scene));
		clear(nodes.overviewCard);
		nodes.overviewCard.appendChild(renderOverviewHeader());
		if (!allResults.length) {
			nodes.overviewCard.appendChild(el("p",{ className: "ci-note", text: "No scenes were recorded in summary.json." }));
			return;
		}
		if (!sceneEntries.length) {
			nodes.overviewCard.appendChild(el("p",{ className: "ci-note", text: "No failing scenes in this report." }));
			return;
		}

		const columns = outputColumns(allResults);
		const table = el("table",{ className: "overview" });
		const headerRow = el("tr",{},el("th",{},hintLabel("Scene",HINTS.overviewScene)));
		for (const column of columns)
			headerRow.appendChild(el("th",{},hintLabel(column.title,outputHint(column.identifier))));
		headerRow.appendChild(el("th",{},hintLabel("Details",HINTS.overviewDetails)));
		table.appendChild(el("thead",{},headerRow));

		const body = el("tbody");
		sceneEntries.forEach(({ scene, sceneIndex }) => {
			const row = el("tr");
			const info = sceneInfo(scene,sceneIndex);
			const sceneStatus = effectiveSceneStatus(scene);
			const sceneCell = el("td",{ className: "scene side-cell" },
				el("div",{ className: "scene-summary" },
					badge(sceneStatus),
					el("div",{ className: "scene-copy" },
						el("div",{ className: "scene-index", text: "Scene " + String(info.index) }),
						el("div",{ className: "scene-title", text: info.name })
					),
					el("div",{ className: "metric", text: "Sensor " + info.sensor })
				)
			);
			row.appendChild(sceneCell);
			for (const column of columns) {
				const image = (scene.array || []).find((entry) => entry.identifier === column.identifier);
				row.appendChild(el("td",{ className: statusClass(image && image.status) + "-cell" },imageCell(image,sceneIndex)));
			}
			const firstImage = (scene.array || [])[0];
			const detailCell = el("td",{ className: "side-cell" },el("div",{ className: "detail-actions" }));
			if (firstImage)
				detailCell.firstChild.appendChild(previewButton("Preview EXR",sceneIndex,firstImage.identifier));
			const detailButton = el("button",{ className: "detail-link link-button", text: "Open details", dataset: { hint: HINTS.detailsButton } });
			detailButton.type = "button";
			detailButton.addEventListener("click",() => {
				const target = document.getElementById(sceneAnchor(scene,sceneIndex + 1));
				if (target)
					target.scrollIntoView({ behavior: "smooth", block: "start" });
			});
			detailCell.firstChild.appendChild(detailButton);
			row.appendChild(detailCell);
			body.appendChild(row);
		});
		table.appendChild(body);
		nodes.overviewCard.appendChild(el("div",{ className: "table-scroll" },table));
	}

	function fallbackCopy(text) {
		const textarea = document.createElement("textarea");
		textarea.value = text;
		textarea.setAttribute("readonly","");
		textarea.style.position = "fixed";
		textarea.style.insetInlineStart = "-100vw";
		document.body.appendChild(textarea);
		textarea.select();
		const copied = document.execCommand("copy");
		textarea.remove();
		if (!copied)
			throw new Error("copy failed");
	}

	async function copyText(text, button) {
		const originalText = button.textContent;
		try {
			if (navigator.clipboard && window.isSecureContext)
				await navigator.clipboard.writeText(text);
			else
				fallbackCopy(text);
			button.textContent = "Copied";
		} catch (_) {
			button.textContent = "Copy failed";
		}
		window.setTimeout(() => {
			button.textContent = originalText;
		},1400);
	}

	function copyPathButton(path) {
		const button = el("button",{ className: "copy-action", text: "Copy", dataset: { hint: HINTS.copyPath } });
		button.type = "button";
		button.addEventListener("click",() => copyText(path,button));
		return button;
	}

	function artifactAction(label, path, sceneIndex, outputIdentifier, variant) {
		const hint = variant === "render" ? HINTS.renderArtifact : variant === "reference" ? HINTS.referenceArtifact : HINTS.differenceArtifact;
		if (path)
			return el("span",{ className: "artifact-action" },
				el("a",{ className: "file-action", href: artifactUrl(path), text: label, dataset: { hint }, attrs: { download: artifactFilename(path) } }),
				copyPathButton(path),
				previewButton("Preview",sceneIndex,outputIdentifier,variant)
			);
		return el("span",{ className: "file-action is-disabled", text: label, dataset: { hint } });
	}

