	function artifactCounts() {
		const counts = {
			outputs: 0,
			render: 0,
			reference: 0,
			difference: 0,
			missingRender: 0,
			missingReference: 0
		};
		for (const scene of state.summary.results || []) {
			for (const image of scene.array || []) {
				++counts.outputs;
				if (image.render)
					++counts.render;
				if (image.reference)
					++counts.reference;
				if (image.difference)
					++counts.difference;
				if (normalizeStatus(image.status) === "missing-render")
					++counts.missingRender;
				if (normalizeStatus(image.status) === "missing-reference")
					++counts.missingReference;
			}
		}
		return counts;
	}

